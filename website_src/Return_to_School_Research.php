                        <strong class="text-primary">We invite you to scroll down and read this guide. The below 3 summaries are not a full representation of the full guide!</strong>

                        </p>


                   <div class="row">

                       <div class="col col-sm-12 col-lg-4">
                           <h4>Abstract</h4>

                           <p style="font-family: 'Roboto', sans-serif;">
                               The total return to normalcy in the workplace, business, and school will not occur overnight; gradual
                               steps must be taken until life as we know it (before COVID-19) returns. This paper discusses a method
                               of gradually returning students to a high school environment, specifically Hillfield Strathallan College
                               (HSC), while reducing the risk of infection and adhering to government guidelines. There is no
                               specified date of this method being implemented as of April 2020. Government response and approval
                               need to be taken into consideration before implementation.
                           </p>
                       </div>

                       <div class="col col-sm-12 col-lg-4">
                           <h4>Introduction</h4>

                           <p style="font-family: 'Roboto', sans-serif;">
                               As the government is concerned with the ongoing financial crisis, the healthcare system, and
                               controlling the spread of COVID-19, it is unlikely that a comprehensive plan for the return to school
                               will be implemented in the near future. As an independent school with additional resources, HSC is
                               able to take the lead in developing a safe way for in-class learning.<br><br>
                               The return to an in-person learning environment should be taken in several steps: low-risk,
                               medium-risk, and high-risk of infection systems. These are defined relative to the number of students
                               present on the school campus and each class, the size of large assemblies and gatherings, personal
                               protective equipment (PPE) for students and staff, the screening of symptoms, and the length of the
                               school day
                           </p>
                       </div>

                       <div class="col col-sm-12 col-lg-4">
                           <h4>Conclusion</h4>

                           <p style="font-family: 'Roboto', sans-serif;">
                               Conclusion
                               With the aforementioned steps taken, a return to school is within the realm of possibility before the
                               end of the 2019/2020 school year. However, there has been no determined date made by the
                               government or health officials when this could occur. It would be recommended that this proposal is
                               approved by these officials before implementation.
                           </p>
                       </div>

