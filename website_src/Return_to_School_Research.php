                    <div class="card card-body">
                        <p>One of our visitors wrote a guide suggesting how students in the Ontario schooling system can return back to schools.<br>
                            Without doubt, the online school experience is much different and possibly less effective then an in class environment. As such, this guide presents a way that students can possibly return to school safely in this school year.
                            <br><i>Please note, this guide has not been peer reviewed, and is not a formally published document. Rather, it is developed by a concerned citizen who is eager to help our community. </i>
                            <br><i>Also, covid19-tracker.ca has no affiliation whatsoever with the author of this document. We have attained permission to display the document on our website, however, we do not endorse nor guarantee nor take any liability in any way for anything due to this document.  </i>
